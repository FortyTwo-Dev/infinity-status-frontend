/**
 * @file theme-types.ts
 * Supported theme colors for the application's UI.
 */

/**
 * These colors define the visual appearance of the site (buttons, backgrounds, etc.).
 *
 * - 'red'   : Red theme (primary color: red, secondary: white/black).
 * - 'blue'  : Blue theme (primary color: blue, secondary: white/black).
 * - 'pink'  : Pink theme (primary color: pink, secondary: white/black).
 * - 'yellow': Yellow theme (primary color: yellow, secondary: black for contrast).
 *
 * @note These themes are applied globally to the site's UI components.
 */
export type SiteThemeColor = 'red' | 'blue' | 'pink' | 'yellow';